#' @title Statlog (Heart) Data Set
#'
#' @description The data consists of 270 patients described by six numerical 
#' variables and eight categorical variables.
#'
#' @format A data frame of 270 rows (patients) and 14 columns (variables):
#' \itemize{
#'   \item{age}{numerical. Age in years.}
#'   \item{resting blood pressure (trestbps)}{numerical. Resting blood pressure (in mmHg) at hospital admittance.}
#'   \item{serum cholesterol (chol)}{numerical. Serum cholesterol in mg/dl.}
#'   \item{maximum heart rate achieved (maxhr)}{numerical. Maximum heart rate achieved during exercise.}
#'   \item{oldpeak (oldpeak)}{numerical. ST depression induced by exercise relative to rest.}
#'   \item{number of major vessels colored by fluoroscopy (numv)}{numerical. Number of major vessels (0-3) colored by fluoroscopy.}
#'   \item{sex}{categorical. Sex (1 = male; 0 = female). }
#'   \item{chest pain type (cp)}{categorical. 1: typical angina, 2: atypical angina, 3: non-anginal pain, 4: asymptomatic.}
#'   \item{fasting blood sugar > 120mg/dl (fbs)}{categorical. 1 = true; 0 = false.}
#'   \item{resting electrocardiographic results (restecg)}{categorical. 0: normal, 1: ST-T wave abnormality (T wave inversions and/or ST
#'    elevation or depression >0.05mV), 2: showing probable or definite left ventricular hypertrophy by Estes' criteria.}
#'   \item{exercice induced angina (exang)}{categorical. 1 = yes; 0 = no. }
#'   \item{the slope of the peak exercice ST segment (slope)}{categorical. 1: upsloping, 2: flat, 3: downsloping.}
#'   \item{thalassemia (thal)}{categorical. 3: normal blood flow, 6: fixed defect, 7: reversible defect.}
#'   \item{presence or absence of heart disease (HD)}{categorical. Absence or presence of a heart disease.}
#' }
#' @source \url{http://archive.ics.uci.edu/ml/datasets/statlog+(heart)}
#' @references A. Frank and A. Asuncion. UCI machine learning repository, statlog (heart) data set, 2010.
